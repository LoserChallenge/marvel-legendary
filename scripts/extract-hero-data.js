const vm = require('vm');
const fs = require('fs');
const path = require('path');

// Load the database file
const dbPath = path.join(
  'Legendary-Solo-Play-main',
  'Legendary-Solo-Play-main',
  'cardDatabase.js'
);
let code = fs.readFileSync(dbPath, 'utf8');

// IMPORTANT: In Node.js vm, `const` declarations are block-scoped to the
// script and NOT accessible on the context object. We replace just the
// `const heroes = ` declaration with a plain assignment so it becomes
// available as context.heroes after execution.
code = code.replace(/\bconst heroes\s*=/, 'heroes =');

const context = { window: {} };
vm.createContext(context);
vm.runInContext(code, context);

const heroes = context.heroes;
if (!heroes) {
  console.error('ERROR: heroes array not found in cardDatabase.js');
  process.exit(1);
}

// Filter to Core Set heroes only (identified by image path)
const coreSetHeroes = heroes.filter(h =>
  h.cards && h.cards.some(c => c.image && c.image.includes('Reskinned Core'))
);

// Derive the base value display string from DB fields
function baseValue(card) {
  const hasAttack = card.attackIcon === true;
  const hasRecruit = card.recruitIcon === true;
  if (!hasAttack && !hasRecruit) return '0';
  const parts = [];
  if (hasAttack) {
    parts.push(card.bonusAttack > 0 ? '0+ [Attack]' : `${card.attack} [Attack]`);
  }
  if (hasRecruit) {
    parts.push(card.bonusRecruit > 0 ? '0+ [Recruit]' : `${card.recruit} [Recruit]`);
  }
  return parts.join(', ');
}

// Build markdown draft and metadata JSON
let markdown = `# Core Set Heroes — Draft\n\n`;
markdown += `<!-- Generated by scripts/extract-hero-data.js -->\n`;
markdown += `<!-- Structured fields from cardDatabase.js. Effect Text column filled in separately. -->\n\n`;
const metadata = {};

for (const hero of coreSetHeroes) {
  markdown += `### ${hero.name}\n\n`;
  markdown += `| Card Name | Class | Team | Cost | Base Value | Effect Text |\n`;
  markdown += `|-----------|-------|------|------|------------|-------------|\n`;

  for (const card of hero.cards) {
    // Strip hero name prefix from card name (e.g. "Black Widow - Mission Accomplished" -> "Mission Accomplished")
    const cardName = card.name.startsWith(`${hero.name} - `)
      ? card.name.slice(hero.name.length + 3)
      : card.name;

    const cls = (card.classes && card.classes.length > 0) ? card.classes[0] : 'None';
    const team = card.team || 'None';
    const cost = card.cost;
    const bv = baseValue(card);

    markdown += `| ${cardName} | ${cls} | ${team} | ${cost} | ${bv} | |\n`;

    metadata[card.name] = {
      image: card.image || '',
      condition: card.condition || 'None',
      conditionType: card.conditionType || 'None'
    };
  }

  markdown += '\n';
}

// Write outputs
const draftPath = path.join('docs', 'card-effects-reference', 'core-set-heroes-draft.md');
const metaPath = path.join('docs', 'card-effects-reference', 'core-set-metadata.json');

fs.writeFileSync(draftPath, markdown);
fs.writeFileSync(metaPath, JSON.stringify(metadata, null, 2));

console.log(`Done. ${coreSetHeroes.length} heroes extracted.`);
console.log(`Draft: ${draftPath}`);
console.log(`Metadata: ${metaPath}`);
